class PetDog(Dog):
    def __init__(self, name, age, weight):
        super().__init__(name, age, weight)
        self.trained = False

    def train(self):
        print(self.bark())
        self.trained = True

    def play(self, *args):
        all_names = [self.name]
        for dog in args:
            all_names.append(dog.name)
        print(f"{all_names} all play together")

    def do_a_trick(self):
        if self.trained:
            tricks = ["does a barrel roll", "stands on his back legs", "shakes your hand", "plays dead"]
            print(f"{self.name} {random.choice(tricks)}")

milou = PetDog("Milou", 3, 10)
rex = PetDog("Rex", 5, 20)
lassie = PetDog("Lassie", 4, 15)
milou.train()
milou.play(rex, lassie)
milou.do_a_trick()

